function applyTranslations(language) {
  const elements = document.querySelectorAll(
    "[data-translate], [data-placeholder]"
  ); // Select elements for text and placeholder translations
  elements.forEach((element) => {
    const translationKey =
      element.getAttribute("data-translate") ||
      element.getAttribute("data-placeholder");

    if (translations[language] && translations[language][translationKey]) {
      let translatedText = translations[language][translationKey]; // Replace line breaks in translations

      translatedText = translatedText.replace(/\n/g, "<br>"); // Handle <input> placeholders

      if (element.tagName === "INPUT" && element.hasAttribute("placeholder")) {
        if (element.hasAttribute("data-placeholder")) {
          element.setAttribute("placeholder", translatedText); // Set placeholder for input fields
        }
      } // Handle <select> elements
      else if (
        element.tagName === "SELECT" &&
        element.hasAttribute("data-placeholder")
      ) {
        // Set the placeholder (first disabled option)
        const firstOption = element.querySelector("option[disabled][selected]");
        if (firstOption) {
          firstOption.textContent = translatedText;
        }
      } // Handle other elements (e.g., <span>, <label>)
      else {
        element.innerHTML = translatedText; // Use innerHTML for text content
      }
    }
  }); // Translate options inside <select> elements

  const options = document.querySelectorAll("select option[data-translate]");
  options.forEach((option) => {
    const optionKey = option.getAttribute("data-translate");
    if (translations[language] && translations[language][optionKey]) {
      option.textContent = translations[language][optionKey];
    }
  });
}

// Load the default language (or the saved one) and apply translations
window.addEventListener("DOMContentLoaded", () => {
  const savedLanguage = localStorage.getItem("preferredLanguage") || "en"; // Default to English if none is set
  console.log("Saved Language on Load:", savedLanguage); // Debugging step
  applyTranslations(savedLanguage); // Apply translations immediately
  setLanguageDirection(savedLanguage); // Set language direction (LTR/RTL) and fonts
});

// Function to switch language (and save to localStorage)
function switchLanguage(language) {
  // Save the chosen language in localStorage
  localStorage.setItem("preferredLanguage", language);

  // Apply translations immediately
  applyTranslations(language);

  // Set language direction and font
  setLanguageDirection(language);

  // Update the language button (if you have a button to show the language)
  const languageButton = document.getElementById("lge-btn");
  languageButton.textContent = language.toUpperCase();
}

// Function to set language direction and font
function setLanguageDirection(language) {
  const body = document.body;

  // Reset classes for direction and font
  body.classList.remove("ltr", "rtl", "font-en", "font-fr", "font-ar");

  // Apply classes based on language
  if (language === "en") {
    body.classList.add("ltr", "font-en");
  } else if (language === "fr") {
    body.classList.add("ltr", "font-fr");
  } else if (language === "ar") {
    body.classList.add("rtl", "font-ar");
  }
}

// Example of switching language when clicking a language button
document.getElementById("lge-btn").addEventListener("click", () => {
  const currentLang = localStorage.getItem("preferredLanguage");
  const newLang =
    currentLang === "en" ? "fr" : currentLang === "fr" ? "ar" : "en"; // Cycle through languages
  switchLanguage(newLang);
});
